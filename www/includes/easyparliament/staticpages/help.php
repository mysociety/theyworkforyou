<dt><a name="coverage"></a>Is this the whole of Hansard?</dt>
<dd>
<p>TheyWorkForYou currently contains:</p>
<ul>
<li>Hansard Commons debates back to the General Election of <strong>1935</strong>;
<li>knowledge of MPs back to <strong>1806</strong> or thereabouts;
<li>Hansard Commons written answers and written ministerial statements
back to the General Election of June 2001;
<li>Hansard Commons Public Bill Committees (previously called Standing Committees) back to the
start of the 2000&ndash;01 session.
<li>Hansard House of Lords Hansard (except Grand Committees) back to around November 1999;
<li>knowledge of Lords back to the House of Lords Act 1999;
<li>everything in the various Northern Ireland (Transitional) Assembly debates, and all MLAs;
<li>everything in the Scottish Parliament official report, and all MSPs.
</ul>

<p>But this is by no means everything. Think of what we've done thus far as a
mere taster of what could be possible.  In the future we&rsquo;d like to
do things such as future business, more coverage of Bills, and so on.
If you want the complete, definitive record, go to the <a
href="http://www.parliament.uk/" title="Link to parliament website">UK
Parliament</a> site, and you might be able to find what you want.</p>
</dd>

<dt><a name="moderation"></a>How do you moderate annotations?</dt>
<dd>
<p>Ideally, we won't. If everyone keeps to the <a
href="http://www.theyworkforyou.com/houserules/" title="link to House
Rules">House Rules</a>, that is. But we're not naive enough to think that life
online is that simple. TheyWorkForYou.com operates a 'reactive moderation'
policy. We will only check whether an annotation breaches our House Rules if
someone lets us know of their concerns via the Report link,
which can be found next to every annotation. If we decide that the annotation has
breached our House Rules, we will delete it and let the original author know
via email. We will also give them opportunity to rephrase and resubmit their
orginal annotation. If we deem the annotation to be legit, we'll leave it up, and
email the complainant to let them know why. We will do our utmost to respond to
reports of potential breaches of our House Rules as soon as we can, but
please bear in mind that this service is run by a tiny charity and some volunteers.
</p> </dd>
