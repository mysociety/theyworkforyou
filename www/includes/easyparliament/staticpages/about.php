<p>We are a dozen or so <a href="#FrancisIsTheMan">volunteers</a> who think it should be really easy for people to keep tabs on their elected MPs, and their unelected Peers, and comment on what goes on in Parliament. We've done this sort of thing <a href="#projects">before</a>, but never on this scale.</p>

<p>For all its faults and foibles, our democracy is a profound gift from previous generations. Yet most people don't know the name of their MP, nor their constituency, let alone what their MP does or says in their name. 
</p>
<p>
We aim to help bridge this growing democratic disconnect, in the belief that there is little wrong with Parliament that a healthy mixture of transparency and public engagement won't fix.</p>

<p>Hence this website.</p>

<a name="projects"></a>
<p>Between us, we've developed and run a range of not-for-profit websites of a vaguely similar ilk. <a href="http://www.publicwhip.org.uk/">PublicWhip.org.uk</a>, <a href="http://www.faxyourmp.com/">FaxYourMP.com</a>, <a href="http://www.pepysdiary.com/">PepysDiary.com</a>, <a href="http://www.haddock.org/">Haddock.org</a>, <a href="http://www.byliner.com/">Byliner.com</a>, <a href="http://www.b3ta.com/">B3Ta</a>, <a href="http://www.downingstreetsays.com/">DowningStreetSays.com</a>, <a href="http://www.mysociety.org/">MySociety</a>,  and highly-accessible re-versionings of the <a href="http://www.dracos.co.uk/odeon/">Odeon Cinema</a> &amp; <a href="http://www.dracos.co.uk/railway/timetable/">National Rail Timetable</a> websites. We conceived and developed the original <a href="http://www.upmystreet.com/">UpMyStreet.com</a> back in <a href="http://web.archive.org/web/19981202165125/http://www.upmystreet.com/">1998</a>. 
</p>
<p>We do all this in our spare time, because we think it's worth doing. And if it's worth doing, we might as well try to do it well. </p>

<a name="FrancisIsTheMan"></a>
<p>It's not strictly true to say that the website was built entirely by volunteer effort. One of our key developers was kept fed for a month by a small grant from a charity called <a href="http://www.democracy.org.uk/">UK Citizen's Online Democracy</a>. This money was vital. Hungry developers don't write good code.
</p>

