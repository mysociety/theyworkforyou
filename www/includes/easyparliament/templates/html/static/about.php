                <h1><?= gettext("About TheyWorkForYou") ?></h1>

                <p><?= gettext('TheyWorkForYou was founded <a href="https://www.mysociety.org/anniversary/">twenty years ago</a> to make Parliament more accessible and accountable.') ?></p>

                <p><?= gettext('We believe that information about our elected representatives should be easily understandable and accessible to everyone, and not just insiders or those who can pay.') ?></p>

                <p><?= gettext("We now work across the UK's Parliaments to bring information together in one place, and make it accessible to both citizens and to civil society.") ?></p>

                <p><?= gettext('Through <a href="https://www.theyworkforyou.com/">TheyWorkForYou</a> and <a href="https://writetothem.com">WriteToThem</a>, we want to make it easy to understand what the UK\'s
                  different layers of representation do, and make the actions of our
                representatives and governments more transparent.') ?></p>

                <p><?= gettext('We do this by:') ?></p>

                <ul>
                  <li><?= gettext('Making parliamentary debates searchable for all Parliaments.') ?></li>
                  <li><?= gettext('Making it easy for people to write to their representatives at any layer of government.') ?></li>
                  <li><?= gettext('Powering <a href="https://www.theyworkforyou.com/alert/">email alerts</a> that let <a href="https://www.mysociety.org/2022/07/28/theyworkforyou-quietly-provides-essential-services-for-civil-society-and-beyond/">citizens and civil society</a> stay informed about their representatives or areas of interest.') ?></li>
                  <li><?= gettext('Adding new information and summaries that build on the information that Parliament releases (such as voting record summaries and more accessible registers of members interests).') ?></li>
                </ul>

                <p><?= gettext('If you support TheyWorkForYou\'s goals, you can help us do more by making a <a href="https://www.mysociety.org/donate/?utm_source=theyworkforyou&utm_medium=website&utm_campaign=theyworkforyou-about-page">donation</a>.') ?></p>

