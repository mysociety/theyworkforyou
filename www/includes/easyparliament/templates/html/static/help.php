            <h2 id="first">First things first</h2>
            <dl>
                <dt id="what">
                    What is TheyWorkForYou?
                </dt>
                <dd>
                    TheyWorkForYou is a website with a simple aim: to make it much easier for anyone to understand exactly what is going on in Parliament.
                </dd>

                <dt id="government">
                    Is it run by the government?
                </dt>
                <dd>
                    No: it&rsquo;s a completely independent project, which has been run by the UK charity <a href="https://www.mysociety.org/">mySociety</a> since 2006.
                </dd>

                <dt id="name">
                    Why is it called TheyWorkForYou?
                </dt>
                <dd>
                    <p>
                        Because that&rsquo;s the fundamental concept of a democracy: we vote our representatives in, and they work on our behalf.
                    </p>

                    <p>
                        Perhaps you feel that your representative isn&rsquo;t, in fact, working very hard or very effectively? Well,
                        that&rsquo;s one thing that TheyWorkForYou can help with: it can show you what your representative is doing
                        &ndash; and if you find that unsatisfactory, it also shows you how to contact them and tell them about it.
                    </p>
                </dd>
            </dl>

            <h2 id="content">Content</h2>
            <dl>
                <dt id="where-content">
                    Where does the content come from?
                </dt>
                <dd>
                    <p>
                        Most of the content you&rsquo;ll see on this site &ndash; the debates, the facts and figures about MPs &ndash;
                        is imported from elsewhere.
                    </p>

                    <p>
                        Debates, for example, come from the official parliamentary Hansard. MPs&rsquo; positions, interests and job titles
                        also come from parliamentary sources.
                    </p>

                    <p>
                        The difference between TheyWorkForYou and the Parliament website? We give the data a bit of a polish: we make it
                        easier to follow debates by highlighting who&rsquo;s speaking, for example, and allow you to search and link to them too.
                    </p>

                    <p>
                        We also perform some automated analysis, for example, MPs&rsquo; pages will tell you how many times they have spoken,
                        and whether that&rsquo;s above or below average.
                    </p>

                    <p>
                        Voting content is taken from <a href="http://www.publicwhip.org.uk/">Public Whip</a>, a separate project (not run by us)
                        which also works with scraped parliamentary data.
                    </p>
                </dd>

                <dt id="archives">
                    Do you keep archives of debates?
                </dt>
                <dd>
                    <p>
                        Yes! That means that TheyWorkForYou is also a rich, accessible historic resource.
                    </p>

                    <p>Here&rsquo;s everything you&rsquo;ll find on the site currently:</p>

                    <ul>
                        <li>House of Commons debates back to the General Election of <strong>1935</strong>;
                        <li>Data on MPs back to <strong>1806</strong> or thereabouts;
                        <li>House of Commons written answers and written ministerial statements
                        back to the General Election of June 2001;
                        <li>House of Commons Public Bill Committees (previously called Standing Committees) back to the
                        start of the 2000&ndash;01 session.
                        <li>House of Lords Hansard (except Grand Committees) back to around November 1999;
                        <li>Data on Lords back to the House of Lords Act 1999;
                        <li>everything in the various Northern Ireland (Transitional) Assembly debates, and all MLAs;
                        <li>everything in the Scottish Parliament official report until August 2014, and all MSPs.
                    </ul>

                    <p>
                    It&rsquo;s all searchable, and when you find part of a debate that interests you, you can share it, because each
                    statement in every debate has its own URL (web address).
                    </p>
                </dd>

                <dt id="found-error">
                    What should I do if I find an error on the site?
                </dt>
                <dt>
                    <p>
                        By all means <a href="mailto:support@theyworkforyou.com">let us know</a>, but be aware that because,
                        as explained above, much of our data replicates the official parliamentary source, the error may well
                        originate from there.
                    </p>

                    <p>
                        Parliament undertakes a rolling process of corrections and updates. When their content is amended, the
                        next time our site picks it up, our content should be amended too.
                    </p>
                </dt>

                <dt id="vote-descriptions">
                    What does it mean when you say that a representative has &ldquo;consistently&rdquo; or &ldquo;generally&rdquo;
                    voted for or against something?
                </dt>

                <dd>
                    <p>
                        MPs&rsquo; stances are made up from a number of different votes which Public Whip have deemed to be relevant
                        to the topic. For example, an MP&rsquo;s position on Trident is judged from the way he or she voted on each
                        occasion <a href="http://www.publicwhip.org.uk/policy.php?id=984">listed here</a>.
                    </p>

                    <p>
                        If an MP&rsquo;s votes all, or almost all, align strongly with those listed on that page, we&rsquo;d say
                        they &ldquo;consistently&rdquo; voted in favour of Trident. MPs who have voted in a different way are marked
                        as having &ldquo;almost always voted for&rdquo;, &ldquo;generally voted against&rdquo;, or &ldquo;consistently
                        voted against&rdquo; the issue, depending on how many of their votes align with this list.
                    </p>

                    <p>
                        You can see a link at the end of every such statement on TheyWorkForYou, titled &ldquo;details&rdquo; &ndash;
                        clicking on that will show you exactly which votes went towards any particular stance.
                    </p>

                    <p>
                        The votes that go to make up each position are carefully compiled by hand: we&rsquo;ve written a couple of
                        blog posts on how we do that: read the <a href="https://www.mysociety.org/2014/07/15/weve-analysed-every-vote-since-the-general-election-and-why-thats-good-news-for-you/">first one here</a>
                        and the <a href="https://www.mysociety.org/2015/08/03/how-far-does-your-mp-tread-the-party-line/">second here</a>.
                    </p>
                </dd>

