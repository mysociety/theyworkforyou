                <h3 class="js-toc-item" id="api">
                    What can I do with the TheyWorkForYou API?
                </h3>

                <p>
                    <a href="https://www.theyworkforyou.com/api/">The API</a> queries the TheyWorkForYou database
                    to return data on MPs or debates. It&rsquo;s free for projects that are both low-volume AND charitable,
                    and there&rsquo;s a small fee attached for any usage outside that.
                </p>

                <p>
                    Here&rsquo;s some of the data you can fetch with the API:
                </p>

                <ul>
                    <li>
                        Details of all MPs, either currently or at any date in the past
                    </li>
                    <li>
                        The boundaries of parliamentary constituencies
                    </li>
                    <li>
                        The content of debates between any two dates that you specify, or containing any keyword that you specify
                    </li>
                    <li>
                        A mixture of the above, for example, MPs&rsquo; names along with their speeches or the constituencies they represent.
                    </li>
                </ul>

                <p>
                    And lots more &ndash; see the whole list on the <a href="/api">API page</a>.
                </p>

                <p>
                    If you are doing large scale analysis of our data, e.g. all uses of a term over a Parliamentary session, then you
                    will probably find our <a href="https://parser.theyworkforyou.com/">raw data</a> more suitable than the API.
                </p>

