                <h3 class="js-toc-item" id="problem-confirmation">
                    I&rsquo;m trying to sign up for an email alert but the confirmation mail has not arrived
                </h3>
                <p>
                    Please check your spam or junk folder. If you still can&rsquo;t find the email,
                    <a href="mailto:support@theyworkforyou.com">let us know</a> and we&rsquo;ll confirm you at our end.
                        </p>

                <h3 class="js-toc-item" id="problem-stop">
                    How can I stop email alerts?
                </h3>
                <p>
                    At the foot of every alert email, you will see a link. Click on this &ndash; no password
                    required &ndash; and to the right of the page, you will see a list of every alert you are
                    subscribed to. For each one, you can pause the alert, or delete it altogether. For more
                    information see our blog post on <a href="https://www.mysociety.org/2014/09/04/how-to-manage-your-theyworkforyou-alerts/">
                    managing your email alerts</a>.
                </p>

                <h3 class="js-toc-item" id="problem-change">
                    How can I change or manage my email alerts?
                </h3>
                <p>
                    <a href="https://www.mysociety.org/2014/09/04/how-to-manage-your-theyworkforyou-alerts/">Check this blog post</a>
                    where we have explained the best way to manage your email alerts.
                </p>

                <h3 class="js-toc-item" id="problem-unrecognised">
                    My postcode isn&rsquo;t recognised by your site
                </h3>
                <p>
                    If your postcode is brand new, it&rsquo;s possible that we don&rsquo;t yet have the data to recognise it.
                    The site will update in due course when we upload new data from Ordnance Survey. We&rsquo;d suggest using
                    a nearby postcode that you know to be in the same constituency.
                </p>

                <p>
                    Otherwise, <a href="mailto:support@theyworkforyou.com">please drop us a line</a> and tell us the postcode
                    you&rsquo;re trying to input.
                </p>

                <h3 class="js-toc-item" id="problem-wrong-mp">
                    My postcode returns the wrong MP
                </h3>

                <p>
                    Our postcode-to-constituency mapping is handled by <a href="https://mapit.mysociety.org/">MapIt</a>,
                    a piece of software which takes the centre point of a postcode and returns the constituency that point
                    is in. In rare cases where postcodes straddle boundaries, this method delivers the wrong results for
                    a handful of people.
                </p>

                <p>
                    At the moment, we can only suggest that you use a neighbouring postcode in order to bring up the right
                    representatives.
                </p>

