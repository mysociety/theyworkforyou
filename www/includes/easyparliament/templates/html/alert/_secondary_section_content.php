<h2>TheyWorkForYou Alerts</h2>
<p>TheyWorkForYou Alerts is a free, but incredibly powerful, service from <a href="https://www.mysociety.org/">mySociety</a>. Follow in a keyword or phrase and we'll email you each time it is mentioned in Parliament; or follow any MP or other representative.</p>
<p>Our alerts inform thousands of individuals and organisations when the issues that matter to them are debated, and voted on.</p>
<p>We are a charity and provide this service at no cost. If you want to invest in making UK politics more transparent and accessible please consider making a donation.</p>
